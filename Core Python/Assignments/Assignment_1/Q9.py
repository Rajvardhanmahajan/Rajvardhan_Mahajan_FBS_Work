#9. Write a program to enter base and height of a triangle and find its area.

# Take input
base = int(input('Enter value of base: '))
height = int(input('Enter value of height: '))

#Perform operation
Area = 1 / 2 * (base * height)

#display result
print(f'Area of a triangle is {Area}. Where base of triangle is {base} and height is {height}.')