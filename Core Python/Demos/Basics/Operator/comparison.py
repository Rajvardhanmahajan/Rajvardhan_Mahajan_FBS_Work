###Relational/comparison operator
x = 10 
y = 20

#1. == (Exact equals to)
print(x == 10)
print(x == '10')

#2.!=
print(x != '10')
print(x != x)

#3. >
print(x > 5)
print(x > 15)

#4. >= (greater than or equals to)
print(x >= 10)
print(x >= 11)


#5. < 
print(x < 11)

#6. <=
print(x <= 5)

